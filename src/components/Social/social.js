const Icons = () => {
  const data = useStaticQuery(graphql`
    query SocialQuery {
      site {
        siteMetadata {
          social {
            linkedin
            orcid
            github
            googlescholar
            publons
          }
        }
      }
    }
  `)

  const social = data.site.siteMetadata?.social

  let icons = [
    {
      src: <Linkedin/>,
      link: `https://www.linkedin.com/in/${social?.linkedin}`,
    },
    {
      src: <Github/>, 
      link: `https://www.github.com/${social?.github}`,
    },
    {
      src: <GoogleScholar/>, 
      link: `https://scholar.google.com/citations?user=${social?.googlescholar}`,
    },
    {
      src: <Orcid/>, 
      link: `https://www.orcid.org/${social?.orcid}`,
    },
    {
      src: <Publons/>,
      link: `https://publons.com/researcher/${social?.publons}`,
    },
  ];

  return (
    icons.map((icon, index) => (
      <StyledSocialLi key={index}>
        <a href={icon.link} target="_blank" rel="noreferrer">
          {icon.src}
        </a>
      </StyledSocialLi>
    ))
  )
}

const Social = () => {
  return (
    <StyledSocialList>
      <Icons/>
    </StyledSocialList>
  )
}

